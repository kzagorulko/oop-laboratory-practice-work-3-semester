package ru.zagorulko.practical.eighth;

public class Address {

    private String cityName;
    private int zipCode;
    private String streetName;
    private int biuldingNumber;
    private char biuldingLetter;
    private int apartamentNumber;

    public String getCityName() {
        return cityName;
    }

    public int getZipCode() {
        return zipCode;
    }

    public String getStreetName() {
        return streetName;
    }

    public int getBiuldingNumber() {
        return biuldingNumber;
    }

    public char getBiuldingLetter() {
        return biuldingLetter;
    }

    public int getApartamentNumber() {
        return apartamentNumber;
    }
}
